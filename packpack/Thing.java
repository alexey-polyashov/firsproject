package packpack;

public class Thing {

    int num;
    int val;
    int cost;

    public Thing(int num, int val, int cost) {
        this.num = num;
        this.val = val;
        this.cost = cost;
    }

    public int getNum() {
        return num;
    }

    public int getVal() {
        return val;
    }

    public int getCost() {
        return cost;
    }
}
